export const encodeMorse = (string) => {
	return string
		.toUpperCase()
		.split("")
		.map((char) => {
			if (parseInt(char)) {
				parseInt(char);
			}
			return toMorse[char] ? `${toMorse[char]} ` : `${char}`;
		})
		.join("")
		.replace(/[ /]{2,}/g, " / ");
};

export const decodeMorse = (morseCode) => {
	return morseCode
		.split(/[ /]{2,}|[/]/)
		.map((word) =>
			word
				.split(" ")
				.map((char) => {
					return fromMorse[char] ? fromMorse[char] : `${char}`;
				})
				.join("")
		)
		.join(" ");
};
